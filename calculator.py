if o == "+":
	print ("Answer is ", i + e)
elif o == "-":
	print ("Answer is ", i - e)
elif o == "*":
	print ("Answer is ", i * e)
elif o == "/":
	print ("Answer is ", i / e)
else:
	print("Use a Valid Operator!")
